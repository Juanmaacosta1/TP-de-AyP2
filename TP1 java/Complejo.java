

public class Complejo { //Clase 'Complejo'

    //Atributos
    private Float x;
    private Float y;

    //Constructor vacio
    public Complejo() {

    }

    //Constructor 1
    public Complejo(Float x, Float y) {
        this.x = x;
        this.y = y;
    }

    //Getters y Setters
    public Float getX() {
        return x;
    }

    public void setX(Float x) {
        this.x = x;
    }

    public Float getY() {
        return y;
    }

    public void setY(Float y) {
        this.y = y;
    }

    

    
    
}
